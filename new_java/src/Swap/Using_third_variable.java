package Swap;

public class Using_third_variable {
	
		public static void main(String[] args) {
		
		
		int a= 200;
		int b=300;
		System.out.println("After swapping number is"+" "+ +a+" "+b);
		int temp;
		temp=a;
		a=b;
		b= temp;
		System.out.println("Before swapping number is"+" "+ +a+" "+b);
		}
		}


